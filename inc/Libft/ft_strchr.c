#include	"libft.h"

char	*ft_strchr(const char *str, int c)
{
	int	i;

	i = -1;
	while (str[++i] != '\0' && str[i] != (char) c)
		;
	if (str[i] != '\0')
		return ((char *)&str[i]);
	else
		return (0);
}
